struct Loan {
    id: u32,
    amount: f32,
    share: u32,
}
impl Loan {
    fn new(id: u32, amount: f32, share: u32)->Self{
        Self{
            id,amount,share
        }
    }fn inc_amount(&mut self)->u32{
        self.amount+=self.share as f32;
        self.amount as u32
    }
}
fn main(){
    let mut l=Loan::new(0,234523.4,30);
    let y=l.inc_amount();
    println!("value {}",y)
}
